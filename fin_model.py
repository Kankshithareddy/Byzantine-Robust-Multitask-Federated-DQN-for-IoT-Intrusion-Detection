def resume_personalized_federated_simulation(num_extra_rounds, clients_ds):
    print("🚀 Phase 2: Resuming Federated RL loop...")

    # Lock Epsilon to 0.05 to prevent random actions (RL Amnesia)
    current_epsilon = 0.05
    history = {'global_loss': [], 'global_reward': []}

    for rnd in range(num_extra_rounds):
        # Shift the round counter so it prints Round 41, Round 42, etc.
        actual_round = rnd + 41
        print(f"\n=== Round {actual_round}/50 ===")
        local_weights  = []
        round_rewards, round_losses = [], []

        for i, ds in enumerate(clients_ds):
            cm       = client_models[i]
            train_fn = client_train_steps[i]
            c_loss, c_reward, batches = 0.0, 0.0, 0

            for b_states, b_targets in ds:
                loss, reward = train_fn(b_states, b_targets, current_epsilon)
                c_loss   += loss.numpy()
                c_reward += reward.numpy()
                batches  += 1

            client_avg_loss = c_loss / max(1, batches)
            client_avg_reward = c_reward / max(1, batches)

            round_losses.append(client_avg_loss)
            round_rewards.append(client_avg_reward)

            w = get_global_weights(cm, global_layer_indices)

            # Simulate Byzantine client (last client)
            if SIMULATE_HIJACKED and i == NUM_CLIENTS - 1:
                w = [lw + np.random.normal(0, 2.0, lw.shape) for lw in w]

            local_weights.append(w)

            print(f"  -> Client {i+1} done | Local Loss: {client_avg_loss:.4f} | Local Reward: {client_avg_reward:.2f}")

        # Aggregate with robust Multi-Krum
        print("  -> Aggregating weights via Multi-Krum...")
        new_w = robust_multi_krum_aggregation(local_weights, NUM_CLIENTS, 1, 3)

        for cm, tm in zip(client_models, target_models):
            set_global_weights(cm, global_layer_indices, new_w)
            set_global_weights(tm, global_layer_indices, new_w)

        avg_l, avg_r = np.mean(round_losses), np.mean(round_rewards)
        print(f"✅ Round {actual_round} Completed | Global Loss: {avg_l:.4f} | Global Reward: {avg_r:.2f} | Epsilon: {current_epsilon:.2f}")

        # Checkpoint: Save all models for round 50
        if actual_round % 10 == 0:
            print(f"💾 Saving Checkpoints for Round {actual_round}...")
            for idx in range(NUM_CLIENTS):
                client_models[idx].save(f"/kaggle/working/checkpoint_round_{actual_round}_client_{idx+1}.keras")

        history['global_loss'].append(avg_l)
        history['global_reward'].append(avg_r)

    print("\n✅ Extended Federated RL Completed!")
    print("🎉 Saving EVERYTHING (All Clients & Targets)...")

    for i in range(NUM_CLIENTS):
        c_path = f"/kaggle/working/final_client_{i+1}_model_50_rounds.keras"
        t_path = f"/kaggle/working/final_target_{i+1}_model_50_rounds.keras"

        client_models[i].save(c_path)
        target_models[i].save(t_path)
        print(f"  💾 Saved Client {i+1} -> {c_path}")

    print("✅ All individual models safely saved to /kaggle/working/!")
    return history

