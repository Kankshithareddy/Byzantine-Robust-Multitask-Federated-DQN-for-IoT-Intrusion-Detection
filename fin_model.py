def create_client_datasets(X, y_bin, y1, y2, y3, num_clients, window_size, batch_size):
    print("Generating Synchronized Time-Series Client Datasets...")
    client_datasets = []
    chunk_size = len(X) // num_clients

    for i in range(num_clients):
        start_idx = i * chunk_size
        end_idx = (i + 1) * chunk_size if i < num_clients - 1 else len(X)

        Xc = X[start_idx:end_idx]
        # Align labels to the LAST element of the window
        yc_bin = y_bin[start_idx:end_idx]
        yc1 = y1[start_idx:end_idx]
        yc2 = y2[start_idx:end_idx]
        yc3 = y3[start_idx:end_idx]

        # Use the built-in target parameter to keep X and Y synced
        ds = tf.keras.utils.timeseries_dataset_from_array(
            data=Xc,
            targets=np.stack([yc_bin, yc1, yc2, yc3], axis=1),
            sequence_length=window_size,
            sequence_stride=1,
            shuffle=False,
            batch_size=batch_size
        )

        client_datasets.append(ds.prefetch(tf.data.AUTOTUNE))

    return client_datasets

