    y3 = MinMaxScaler().fit_transform(y3_raw).flatten().astype(np.float32)
    y3 = np.where(y1 == BENIGN_IDX_T1, 0.0, y3)

    # Scale Features
    scaler = MinMaxScaler()
    X = scaler.fit_transform(df[features_to_keep].values.astype(np.float32))

    return X, y_bin, y1, y2, y3

