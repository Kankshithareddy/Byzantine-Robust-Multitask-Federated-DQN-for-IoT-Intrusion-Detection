@tf.function
def get_reward_tf(actions, labels):
    """
    Actions: 0 (Allow), 1 (Rate-Limit), 2 (Drop), 3 (Request Label - Active Learning)
    """
    actions, labels = tf.cast(actions, tf.int32), tf.cast(labels, tf.int32)

    # Reward mapping if True Label is an Attack (1)
    # Heavy penalty for Allow (False Negative), Reward for Drop (True Positive)
    attack_rewards = tf.where(actions == 0, -150.0,
                        tf.where(actions == 1, 10.0,
                            tf.where(actions == 2, 50.0, -0.05))) # -0.05 is the Active Learning cost

    # Reward mapping if True Label is Benign (0)
    # Reward for Allow, Reduced penalty for Rate-Limit, Penalty for Drop (False Positive)
    benign_rewards = tf.where(actions == 0, 10.0,
                        tf.where(actions == 1, -2.0,
                            tf.where(actions == 2, -20.0, -0.05)))

    return tf.where(labels == 1, attack_rewards, benign_rewards)

# ── LOSS WEIGHTS ───────────────────────────────────────────────────────────────
# DQN already scaled by *0.01 internally, so 0.5 here is still meaningful.
# T3 MSE operates on [0,1] values — raw loss is tiny, so weight must be higher
# than intuition suggests to avoid the "always predict 0" lazy solution.
W_DQN = 0.5
W_T1  = 0.8
W_T2  = 1.0
W_T3  = 0.3
W_SAE = 0.05

# ── SUPERVISED WARM-UP ────────────────────────────────────────────────────────
# Run this BEFORE the FL loop to give T1/T2/T3 heads a clean start,
# so early random RL actions don't corrupt classification gradients.
def pretrain_supervised(model, dataset, epochs=5, lr=1e-3):
    optimizer = tf.keras.optimizers.Adam(learning_rate=lr)
    print(f"  Pre-training supervised heads for {epochs} epochs...")
    for epoch in range(epochs):
        total_loss, batches = 0.0, 0
        for states, targets in dataset:
            y1 = tf.cast(targets[:, 1], tf.int32)
            y2 = tf.cast(targets[:, 2], tf.int32)
            y3 = tf.cast(targets[:, 3], tf.float32)

            with tf.GradientTape() as tape:
                _, p1, p2, p3, _ = model(states, training=True)

                loss_t1 = tf.reduce_mean(
                    tf.keras.losses.sparse_categorical_crossentropy(y1, p1))

                valid_mask = tf.not_equal(y2, -1)
                loss_t2 = tf.cond(
                    tf.reduce_any(valid_mask),
                    lambda: tf.reduce_mean(
                        tf.keras.losses.sparse_categorical_crossentropy(
                            tf.boolean_mask(y2, valid_mask),
                            tf.boolean_mask(p2, valid_mask))),
                    lambda: tf.constant(0.0)
                )

                # Attack-only T3: only compute regression loss on attack windows
                attack_mask_t3 = tf.not_equal(y3, 0.0)
                loss_t3 = tf.cond(
                    tf.reduce_any(attack_mask_t3),
                    lambda: tf.reduce_mean(
                        tf.square(tf.boolean_mask(y3, attack_mask_t3) -
                                  tf.squeeze(tf.boolean_mask(p3, attack_mask_t3), axis=1))),
                    lambda: tf.constant(0.0)
                )

                loss = (W_T1 * loss_t1) + (W_T2 * loss_t2) + (W_T3 * loss_t3)

            grads = tape.gradient(loss, model.trainable_variables)
            optimizer.apply_gradients(zip(grads, model.trainable_variables))
            total_loss += loss.numpy()
            batches += 1

        print(f"    Warm-up epoch {epoch+1}/{epochs} — loss: {total_loss/max(1,batches):.4f}")


# ── RL TRAIN STEP ─────────────────────────────────────────────────────────────
def create_client_train_step(model, target_model, optimizer):
    @tf.function
    def train_rl_step(states, targets, epsilon):
        y_bin = targets[:, 0]
        y1    = tf.cast(targets[:, 1], tf.int32)
        y2    = tf.cast(targets[:, 2], tf.int32)
        y3    = tf.cast(targets[:, 3], tf.float32)

        batch_size = tf.shape(states)[0]
        q_preds, _, _, _, _ = model(states, training=False)

        # Epsilon-Greedy action selection
        rand_vals      = tf.random.uniform([batch_size])
        random_actions = tf.random.uniform([batch_size], minval=0,
                                           maxval=NUM_ACTIONS, dtype=tf.int32)
        greedy_actions = tf.cast(tf.argmax(q_preds, axis=1), tf.int32)
        actions        = tf.where(rand_vals < epsilon, random_actions, greedy_actions)

        rewards = get_reward_tf(actions, y_bin)

        # Bellman target
        next_q_preds, _, _, _, _ = target_model(states, training=False)
        max_next_q   = tf.reduce_max(next_q_preds, axis=1)
        target_q_vals = tf.stop_gradient(
            tf.cast(rewards + GAMMA * max_next_q, tf.float32))

        with tf.GradientTape() as tape:
            q_values, p1, p2, p3, sae_recon = model(states, training=True)

            # DQN loss (already small due to *0.01)
            masks    = tf.one_hot(actions, NUM_ACTIONS)
            q_action = tf.reduce_sum(q_values * masks, axis=1)
            loss_dqn = tf.reduce_mean(tf.square(target_q_vals - q_action)) * 0.01

            # T1 loss
            loss_t1 = tf.reduce_mean(
                tf.keras.losses.sparse_categorical_crossentropy(y1, p1))

            # T2 loss — attack windows only
            valid_mask = tf.not_equal(y2, -1)
            loss_t2 = tf.cond(
                tf.reduce_any(valid_mask),
                lambda: tf.reduce_mean(
                    tf.keras.losses.sparse_categorical_crossentropy(
                        tf.boolean_mask(y2, valid_mask),
                        tf.boolean_mask(p2, valid_mask))),
                lambda: tf.constant(0.0)
            )

            # T3 loss — attack windows only (avoids zero-predictor reward on benign)
            attack_mask_t3 = tf.not_equal(y3, 0.0)
            loss_t3 = tf.cond(
                tf.reduce_any(attack_mask_t3),
                lambda: tf.reduce_mean(
                    tf.square(tf.boolean_mask(y3, attack_mask_t3) -
                              tf.squeeze(tf.boolean_mask(p3, attack_mask_t3), axis=1))),
                lambda: tf.constant(0.0)
            )

            # SAE reconstruction loss
            states_f32 = tf.cast(states, tf.float32)
            loss_sae   = tf.reduce_mean(tf.square(states_f32 - sae_recon))

            total_loss = (W_DQN * loss_dqn) + (W_T1 * loss_t1) + \
                         (W_T2 * loss_t2) + (W_T3 * loss_t3) + (W_SAE * loss_sae)

        grads = tape.gradient(total_loss, model.trainable_variables)
        optimizer.apply_gradients(zip(grads, model.trainable_variables))

        return loss_dqn, tf.reduce_mean(rewards)

    return train_rl_step

def robust_multi_krum_aggregation(weight_list, num_clients, num_malicious, multi_krum_m):
    # 1. Flatten nested model weights into 1D vectors for distance math
    flat_weights = [np.concatenate([w.flatten() for w in client_w]) for client_w in weight_list]
    flat_weights = np.array(flat_weights)

    num_to_keep = num_clients - num_malicious - 2
    scores = []

    # 2. Calculate distances to find honest clients
    for i in range(num_clients):
        distances = np.sum((flat_weights - flat_weights[i])**2, axis=1)
        closest_distances = np.sort(distances)[1:num_to_keep + 1]
        scores.append(np.sum(closest_distances))

    # 3. Select 'm' best clients and average them
    best_indices = np.argsort(scores)[:multi_krum_m]
    avg_flat_weight = np.mean(flat_weights[best_indices], axis=0)

    # 4. Reshape back into original model structure
    new_weights = []
    ptr = 0
    for template_w in weight_list[0]:
        size = np.prod(template_w.shape)
        new_weights.append(avg_flat_weight[ptr:ptr + size].reshape(template_w.shape))
        ptr += size

    return new_weights

