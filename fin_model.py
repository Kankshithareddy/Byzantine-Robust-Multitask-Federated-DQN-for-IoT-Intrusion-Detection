def build_refined_defense_model(input_shape, num_actions):
    inputs = layers.Input(shape=input_shape)

    # Stream 1: Temporal (Conv1D + MultiHead Attention)
    x1 = layers.Conv1D(64, kernel_size=3, padding='same', activation='relu')(inputs)
    attn_out = layers.MultiHeadAttention(num_heads=4, key_dim=16)(x1, x1)
    x1 = layers.GlobalAveragePooling1D()(attn_out)

    # Stream 2: SAE — reduced L1 from 0.05 → 0.005 to avoid zero-predictor trap
    x2 = layers.TimeDistributed(layers.Dense(32, activation='relu'))(inputs)
    latent = layers.TimeDistributed(
        layers.Dense(8, activation='relu',
                     activity_regularizer=tf.keras.regularizers.l1(0.005))
    )(x2)

    # Decoder for Reconstruction
    x_dec = layers.TimeDistributed(layers.Dense(32, activation='relu'))(latent)
    reconstructed = layers.TimeDistributed(
        layers.Dense(input_shape[1], activation='sigmoid'), name='sae_reconstruction'
    )(x_dec)

    state_repr_sae = layers.Flatten()(latent)
    combined = layers.Concatenate()([x1, state_repr_sae])
    dense_shared = layers.Dense(128, activation='relu')(combined)

    # ── Task-specific neck layers ──────────────────────────────────────────────
    # Each head gets its own private pathway so objectives don't conflict
    dqn_neck = layers.Dense(64, activation='relu')(dense_shared)
    t1_neck  = layers.Dense(64, activation='relu')(dense_shared)
    t2_neck  = layers.Dense(128, activation='relu')(dense_shared)  # larger: 19-class is hardest
    t3_neck  = layers.Dense(32,  activation='relu')(dense_shared)

    # ── Output heads ──────────────────────────────────────────────────────────
    q_values = layers.Dense(num_actions, name='dqn_action')(dqn_neck)
    t1_out   = layers.Dense(6,  activation='softmax', name='t1_type')(t1_neck)
    t2_out   = layers.Dense(19, activation='softmax', name='t2_subtype')(t2_neck)
    t3_out   = layers.Dense(1,  activation='sigmoid', name='t3_intensity')(t3_neck)
    # sigmoid on T3 keeps predictions in [0,1] matching the MinMaxScaler target range

    return tf.keras.Model(
        inputs=inputs,
        outputs=[q_values, t1_out, t2_out, t3_out, reconstructed]
    )

