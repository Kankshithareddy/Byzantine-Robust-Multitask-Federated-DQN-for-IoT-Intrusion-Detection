def load_and_preprocess_parquet(file_path):
    print(f"Loading data from {file_path}...")
    df = pd.read_parquet(file_path, engine='pyarrow')
    df = df.fillna(df.median(numeric_only=True))

    # Create Labels
    y2 = df['label'].astype(int).values
    y1 = np.vectorize(label_remapping.get)(y2)
    y_bin = (y1 > 0).astype(np.int32) # 0 = Benign, 1 = Attack
    y2 = np.where(y1 == BENIGN_IDX_T1, -1, y2) # Mask T2 benign

    # Task 3: Intensity (Scaled to 0-1 and masked for Benign)
