def get_global_weights(model, layer_indices):
    weights = []
    for idx in layer_indices:
        weights.extend(model.layers[idx].get_weights())
    return weights

def set_global_weights(model, layer_indices, new_weights):
    w_idx = 0
    for l_idx in layer_indices:
        layer = model.layers[l_idx]
        num_weights = len(layer.weights)
        layer.set_weights(new_weights[w_idx : w_idx + num_weights])
        w_idx += num_weights

